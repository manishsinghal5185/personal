package com.capgemini.commons;

public class sparkConfigParam {
    private String sparkConfigName;
    private String sparkConfigValue;
    private String sparkConfigType;

    public String getSparkConfigName() {
        return sparkConfigName;
    }

    public void setSparkConfigName(String sparkConfigName) {
        this.sparkConfigName = sparkConfigName;
    }

    public String getSparkConfigValue() {
        return sparkConfigValue;
    }

    public void setSparkConfigValue(String sparkConfigValue) {
        this.sparkConfigValue = sparkConfigValue;
    }

    public String getSparkConfigType() {
        return sparkConfigType;
    }

    public void setSparkConfigType(String sparkConfigType) {
        this.sparkConfigType = sparkConfigType;
    }
}
